public class Point {
    double x;
    double y;
    double z;
    Point(){
    }
    Point(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public double getX(double x){
        return x;
    }
    public void setX(){
        this.x = x;
    }
    public double getY(double y){
        return y;
    }
    public void setY(){
        this.y = y;
    }
    public double getZ(double z){
        return z;
    }
    public void setZ(){
        this.z = z;
    }
}
